// FUNÇÃO QUE ANIMA O TEXTO COMO SE FOSSE UM ROBÔ DIGITANDO
void animarTexto(const char* texto, int velocidade) {
    for (int i = 0; i < strlen(texto); i++) {
        printf("%c", texto[i]);
        fflush(stdout);
        usleep(velocidade * 1000);
    }
}


